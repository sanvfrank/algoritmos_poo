

public class Pastel {
double gramos;
int cantClientes, cantPasteles;




public Pastel(double gramos, int cantClientes, int cantPasteles) {
    this.gramos = gramos;
    this.cantClientes = cantClientes;
    this.cantPasteles = cantPasteles;
}





public int getCantClientes() {
    return cantClientes;
}





public double getGramos() {
    return gramos;
}





public int getCantPasteles() {
    return cantPasteles;
}



}
